const CV_FILES = {
  es: "Fernando_Aldama_CV_ES.pdf",
  en: "Fernando_Aldama_CV.pdf",
};
const STORAGE_KEY = "portfolio-lang";

function getLang() {
  return localStorage.getItem(STORAGE_KEY) || "en";
}

function setLang(lang) {
  localStorage.setItem(STORAGE_KEY, lang);
  document.documentElement.lang = lang === "es" ? "es" : "en";
  document.title = translations[lang]["meta.title"];
  applyTranslations(lang);
  updateCvLink(lang);
  updateLangSelector(lang);
}

function applyTranslations(lang) {
  const t = translations[lang];
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (t[key]) {
      let text = t[key];
      if (key === "footer.copyright") {
        text = text.replace("{year}", new Date().getFullYear());
      }
      el.textContent = text;
    }
  });
  document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
    const key = el.getAttribute("data-i18n-alt");
    if (t[key]) el.alt = t[key];
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    if (t[key]) el.setAttribute("aria-label", t[key]);
  });
}

function updateCvLink(lang) {
  const cvFile = CV_FILES[lang];
  const cvLink = document.getElementById("cv-download");
  const footerCvLink = document.getElementById("footer-cv-link");
  const mobileCvLink = document.getElementById("cv-download-mobile");
  if (cvLink) cvLink.href = cvFile;
  if (footerCvLink) footerCvLink.href = cvFile;
  if (mobileCvLink) mobileCvLink.href = cvFile;
}

function updateLangSelector(lang) {
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    const btnLang = btn.getAttribute("data-lang-btn");
    btn.classList.toggle("text-primary", btnLang === lang);
    btn.classList.toggle("font-bold", btnLang === lang);
    btn.classList.toggle("text-slate-500", btnLang !== lang);
  });
}

function init() {
  const lang = getLang();
  setLang(lang);
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    btn.addEventListener("click", () =>
      setLang(btn.getAttribute("data-lang-btn")),
    );
  });
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", init);
} else {
  init();
}
